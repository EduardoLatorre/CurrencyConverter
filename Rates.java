public class Rates {
    float USD;
    float AUD;
    float CAD;
    float PLN;
    float MXN;

    public float getUSD() {
        return USD;
    }

    public void setUSD(float USD) {
        this.USD = USD;
    }

    public float getAUD() {
        return AUD;
    }

    public void setAUD(float AUD) {
        this.AUD = AUD;
    }

    public float getCAD() {
        return CAD;
    }

    public void setCAD(float CAD) {
        this.CAD = CAD;
    }

    public float getPLN() {
        return PLN;
    }

    public void setPLN(float PLN) {
        this.PLN = PLN;
    }

    public float getMXN() {
        return MXN;
    }

    public void setMXN(float MXN) {
        this.MXN = MXN;
    }
}
